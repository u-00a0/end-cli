import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { unified } from "unified";
import remarkParse from "remark-parse";
import remarkMath from "remark-math";
import remarkRehype from "remark-rehype";
import rehypeKatex from "rehype-katex";
import rehypeStringify from "rehype-stringify";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const webRoot = path.resolve(scriptDir, "..");
const repoRoot = path.resolve(webRoot, "..");

const sourcePath = path.resolve(repoRoot, "docs/blogs/model_v1.md");
const outputPath = path.resolve(webRoot, "src/lib/generated/modelV1.ts");

export function replaceEscapedDollarInsideMath(markdownSource) {
  // remark-math's `$...$` scanning can treat `\$` inside math as a closing `$`.
  // Work around it by rewriting `\$` to a KaTeX-supported form that does not
  // contain a literal `$` character.
  //
  // This must only run *inside* math segments; in normal Markdown text `\$`
  // should stay as-is and render to a plain `$`.
  const len = markdownSource.length;
  let i = 0;
  let out = "";
  /** @type {"text" | "inlineMath" | "blockMath" | "codeFence"} */
  let mode = "text";

  while (i < len) {
    if (mode === "codeFence") {
      const nextFence = markdownSource.indexOf("```", i);
      if (nextFence === -1) {
        out += markdownSource.slice(i);
        break;
      }
      out += markdownSource.slice(i, nextFence + 3);
      i = nextFence + 3;
      mode = "text";
      continue;
    }

    if (mode === "text") {
      if (markdownSource.startsWith("```", i)) {
        out += "```";
        i += 3;
        mode = "codeFence";
        continue;
      }

      if (markdownSource[i] === "`") {
        // Inline code span: copy verbatim until matching backticks.
        let tickCount = 1;
        while (i + tickCount < len && markdownSource[i + tickCount] === "`") tickCount++;
        const opener = "`".repeat(tickCount);
        const start = i;
        i += tickCount;
        const end = markdownSource.indexOf(opener, i);
        if (end === -1) {
          out += markdownSource.slice(start);
          break;
        }
        out += markdownSource.slice(start, end + tickCount);
        i = end + tickCount;
        continue;
      }

      // Enter math. Ignore escaped dollars (preceded by backslash).
      if (markdownSource.startsWith("$$", i) && markdownSource[i - 1] !== "\\") {
        out += "$$";
        i += 2;
        mode = "blockMath";
        continue;
      }
      if (markdownSource[i] === "$" && markdownSource[i - 1] !== "\\") {
        out += "$";
        i += 1;
        mode = "inlineMath";
        continue;
      }

      out += markdownSource[i];
      i += 1;
      continue;
    }

    // In math: rewrite `\$` and detect closing delimiter.
    if (markdownSource.startsWith("\\$", i)) {
      out += "\\char36";
      i += 2;
      continue;
    }

    if (mode === "blockMath") {
      if (markdownSource.startsWith("$$", i) && markdownSource[i - 1] !== "\\") {
        out += "$$";
        i += 2;
        mode = "text";
        continue;
      }
      out += markdownSource[i];
      i += 1;
      continue;
    }

    // inlineMath
    if (markdownSource[i] === "$" && markdownSource[i - 1] !== "\\") {
      out += "$";
      i += 1;
      mode = "text";
      continue;
    }
    out += markdownSource[i];
    i += 1;
  }

  return out;
}

function toGeneratedModule(markdownSource, renderedHtml) {
  return [
    "// This file is auto-generated by web/scripts/gen-model-v1.mjs",
    "// Do not edit manually.",
    "",
    `export const MODEL_V1_SOURCE_MARKDOWN = ${JSON.stringify(markdownSource)};`,
    "",
    `export const MODEL_V1_RENDERED_HTML = ${JSON.stringify(renderedHtml)};`,
    "",
  ].join("\n");
}

export async function main() {
  const markdownSource = await readFile(sourcePath, "utf8");
  const markdownForRender = replaceEscapedDollarInsideMath(markdownSource);
  const renderedHtml = String(
    await unified()
      .use(remarkParse)
      .use(remarkMath)
      .use(remarkRehype)
      .use(rehypeKatex, {
        throwOnError: false,
        strict: "ignore",
        output: "htmlAndMathml",
      })
      .use(rehypeStringify)
      .process(markdownForRender),
  );

  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(outputPath, toGeneratedModule(markdownSource, renderedHtml), "utf8");
}

const thisFile = fileURLToPath(import.meta.url);
const invokedFile = process.argv[1] ? path.resolve(process.argv[1]) : null;
if (invokedFile === thisFile) {
  await main();
}
